import 'package:user/app/backend/api/api.dart';
import 'package:user/app/helper/shared_pref.dart';
import 'package:get/get.dart';
import 'package:user/app/util/constant.dart';

class OrderParser {
  final ApiService apiService;
  final SharedPreferencesManager sharedPreferencesManager;

  OrderParser({required this.apiService, required this.sharedPreferencesManager});

  Future<Response> getOrder(int limit) async {
    limit = limit * 10;
    return await apiService.postPrivate(AppConstants.getOrders, {'limit': limit, 'id': sharedPreferencesManager.getString('uid')}, sharedPreferencesManager.getString('token') ?? '');
  }

  bool haveLoggedIn() {
    return sharedPreferencesManager.getString('uid') != '' && sharedPreferencesManager.getString('uid') != null ? true : false;
  }

  String getCurrencySide() {
    return sharedPreferencesManager.getString('currencySide') ?? AppConstants.defaultCurrencySide;
  }

  String getCurrencySymbol() {
    return sharedPreferencesManager.getString('currencySymbol') ?? AppConstants.defaultCurrencySymbol;
  }
}
